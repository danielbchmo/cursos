/* Condicional Simple */

let num = 5;

if(num>0){
    console.log(`${num} es mayor que 0`);
}

/* Condicional Compuesta */

if(num>0){
    console.log(`${num} es mayor que 0`);
} else {
    console.log(`${num} es mayor que 0`);
}

/* Condicional Multiple */

if(num>0){
    console.log(`${num} es mayor que 0`);
} else if (num<0) {
    console.log(`${num} es mayor que 0`);
    }
else {
    console.log('No aplica');
}